def height(root):
    maxDepth = 0
    maxLeft = 0
    maxRight = 0
    
    if (root) :
        
        if (root.left) :
            maxLeft = 1
            maxLeft += height(root.left)
        
        if (root.right) :
            maxRight = 1
            maxRight += height(root.right)
        
        maxDepth = max(maxLeft, maxRight)
        
        return maxDepth
    
    


